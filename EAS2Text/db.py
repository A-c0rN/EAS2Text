    def set_db_settings(self, country: str = "US", alt: bool = False):
        """Set Settings for accessing Database.

        Args:
            country (str, optional): Country to use, "US" or "CA". Defaults to "US".
            alt (bool, optional): Use alternate (old) strings. Defaults to False.)
        """
        if country != "US" and alt != False:
            if country in self.countries:
                self.country = country
            self.alt = alt

    def get_loc_from_same(
        self, fips: str, expanded_search: bool = False, country: str = "US"
    ):
        loc = {
            "fips": "",
            "type": "",
            "country": {"name": "", "abbrev": ""},
            "region": {
                "name": "",
                "abbrev": "",
                "code": "",
            },
            "subdivision": "",
        }
        if country != self.country:
            _country = country
        else:
            _country = self.country
        try:
            ## Make sure the code is numeric
            assert fips.isnumeric()
            ## Make sure the code is 5 or 6 chars long
            assert len(fips) >= 5 and len(fips) <= 6
            ## Zfill to make sure in range
            _fips = fips.zfill(6)
            ## Get Sub, Reg, Code from SAME FIPS
            _subdiv = _fips[0]
            _region = _fips[1:3]
            _code = _fips[3:6]
            try:
                loc["fips"] = _fips
                loc["subdivision"] = self.subdivisions[_subdiv]
                if not expanded_search:
                    loc["country"]["name"] = self.samecodes[_country]["name"]
                    loc["country"]["abbrev"] = self.samecodes[_country][
                        "abbrev"
                    ]
                    loc["type"] = self.samecodes[_country][_region]["type"]
                    loc["region"]["name"] = self.samecodes[_country][_region][
                        "name"
                    ]
                    loc["region"]["abbrev"] = self.samecodes[_country][
                        _region
                    ]["abbrev"]
                    loc["region"]["code"] = self.samecodes[_country][_region][
                        "codes"
                    ][_code]
            except:
                pass

        except:
            pass
